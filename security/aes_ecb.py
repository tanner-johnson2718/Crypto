    keys_words = [0] * 11 * 4

    # Copy over the byte array key into the first 4 words of the keys_words
    for i in range(0, 4):
        keys_words[i] = word(key[4*i], key[4*i + 1], key[4*i + 2], key[4*i + 3])

    # the actual round expansion
    i = 4
    while i < 44:
        temp = keys_words[i-1]
        
