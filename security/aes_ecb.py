def poly_multi(a, b):
    result = 0
    while a and b:
        if a & 1: result ^= b
        a >>= 1; b <<= 1
    return result

def poly_mod(a, b):
    bl = b.bit_length()
    while True:
        shift = a.bit_length() - bl
        if shift < 0: return a
        a ^= b << shift

def gf_multi(a,b):
    return poly_mod(poly_multi(a, b), 0x11b)

def gf_add(a,b):
    return a ^ b


